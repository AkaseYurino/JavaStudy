package bronze.purple.chapter2;

public class Sample2_1 {
    public static void main(String[] args){
        System.out.println(255); //10進数：255
        System.out.println(0377); //8進数：0377
        System.out.println(0xff); //16進数：0xff
        System.out.println(0b101); //2進数：0b101
        System.out.println(12.33); //
        System.out.println(3e4);
        System.out.println('A');
        System.out.println('\u3012');
        System.out.println("Hello");
        System.out.println(true);

    }
}
